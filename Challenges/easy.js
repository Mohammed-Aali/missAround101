//A decimal number can be represented as a sequence of bits. To illustrate:
function bitwiseAND(n1, n2) {
	return (n1 & n2);
}

function bitwiseOR(n1, n2) {
	return (n1 | n2);
}

function bitwiseXOR(n1, n2) {
	return (n1 ^ n2);
}
console.log(bitwiseOR(6, 8))